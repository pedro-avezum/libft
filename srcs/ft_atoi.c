int atoi(const char *nptr)
{
	int cnvrtd;
	int sign;
	int i;
	
	i = 0;
	sign = 1;
	while (nptr[i] != '\0')
	{
		if (nptr[i] == '-')
		{
			sign = sign * -1;
		}
	i++;
	}
	i = 0;
	
	return (cnvrtd);
}
